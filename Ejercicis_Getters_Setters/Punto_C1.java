package Ejercicis_Getters_Setters;

public class Punto_C1 {

	private int x;
	private int y;
	
public Punto_C1 (int x, int y) {
		
		this.x = x;
		this.y = y;
	
}

	public int getX() {
		return x;
	}
	public int getY() {
		return y;
	}
	public void setX(int x) {
		this.x = x;
	}
	public void setY(int y) {
		this.y = y;
	}


}
